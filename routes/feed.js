const express = require('express');
const router = express.Router();
const feedController = require('../controllers/feed.js');
const postSchema = require('../validations/postSchema.js');
const { validate } = require('../util/validate');
const isAuth = require('../middlewares/is-auth');
router.get('/posts',isAuth,feedController.index);
router.post('/posts',isAuth,validate(postSchema),feedController.store);
router.get('/posts/:id',isAuth,feedController.show);
router.put('/posts/:id',isAuth,validate(postSchema),feedController.update);
router.delete('/posts/:id',isAuth,feedController.delete);
module.exports = router;